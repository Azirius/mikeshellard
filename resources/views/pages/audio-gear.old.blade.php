      <h2>Audio Sources</h2>
      <p>
        Okay, so audio sources are what they sound like: The source in which the audio comes from. Teems of people simply use their
        mobile phones with a set of earphones that come with their devices. Some people don't. Since Apple removed the 3.5mm headphone 
        out on their iPhones people are more likely to use Bluetooth listening devices to hear their tunes, especially now other OEMs are 
        following suit. There are other options:
        <ol>
          <li>Use a phone with a 3.5mm output instead of one without.</li>
          <li>Attach a cable, such as a Lightning to 3.5mm jack to enable wired usage.</li>
          <li>Attach an external DAC/Amp.</li>
          <li>Or use a DAP (Digital Audio Player).</li>
        </ol>
        <br>
        I have done a mixture of the above, apart from using a phone with a 3.5mm jack. Each have their pros and cons.
        <br>
        <br>
        Attaching an external DAC/Amp to your phone can drain its battery faster, and it means having a 'heavy' peripheral attached to it. 
        The upside being that mobile phones can often stream using the mobile data allowance, plus you don't have to manage another library 
        of music files on another device.
        <br>
        <br>
        Using a cable such as the aforementioned Lightning to 3.5mm jack, is simple and easy to use. It's a very lightweight solution. However,
        if you're planning on using a harder to drive set of headphones, you probably won't get the most out of them this way. Plus, the DAC 
        chip in your mobile device might not be as good as you want it to be.
        <br>
        <br>
        Using a dedicated solution like a DAP usually means you don't need an external amplifier to reach higher volumes, their hardware is more specifically developed for higher end gear and the ability to push headphones harder than normal mobile phones.
        <br>
        They also have other benefits such as dedicated storage for music, can stream on WiFi. Most newer DAPs have the ability to connect to Bluetooth headphones also, if you don't want cables.
        <br>
        The DAC chips inside of DAPs are generally of a higher quality than the ones in your mobile phone also, and can allow for higher resolution
        files to be played (DSD, HiRes Flac, etc.).
        <br>
        It's also nice that using a DAP leaves your phones battery alone and means it can focus on 
        doing more phone-esque things. 
        <br>
        <br>
        There are downsides to using a DAP, however. The fact they can be fairly expensive, especially alongside that new shiny mobile phone you've purchased. Then you have to manage another seperate device for all your music. Then while having a device specifically for 
        your music means that you don't have to use your phones battery, its also another device you have to remember to keep charged and having a 
        means to charge the device, too. All the downsides are pretty negligable and probably won't disuade you if you've already looked into getting a DAP.
